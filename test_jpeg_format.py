def test_file_extension_consistency():
    """Test file extension consistency"""
    print("\n" + "=" * 70)
    print("Test 3: File extension consistency check")
    print("=" * 70)
    
    import inspect
    
    # Check deconstruct function
    deconstruct_source = inspect.getsource(pipeline.deconstruct_pdf_to_images)
    
    # Extract the generated file extension
    gen_ext = None
    if '*.jpg' in deconstruct_source:
        gen_ext = '.jpg'
    elif '*.tif' in deconstruct_source:
        gen_ext = '.tif'
    elif '*.png' in deconstruct_source:
        gen_ext = '.png'
    
    print(f"File type generated by deconstruct: {gen_ext}")
    
    # Check whether the reconstruct function can handle it correctly
    reconstruct_source = inspect.getsource(pipeline.reconstruct_pdf)
    
    if 'suffix' in reconstruct_source or 'ext' in reconstruct_source:
        print(f" ✓ reconstruct dynamically identifies file types (compatible with all formats)")
        return True
    elif gen_ext and gen_ext in reconstruct_source:
        print(f" ✓ reconstruct hardcoded match {gen_ext}")
        return True
    else:
        print(f" ✗ reconstruct may not handle {gen_ext} files correctly")
        return False


