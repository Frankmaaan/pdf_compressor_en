"""
JPEG format switching verification script

Verify that the code modification is correct:
1. Whether deconstruct_pdf_to_images() uses JPEG format
2. Whether reconstruct_pdf() correctly handles JPEG files
3. Does the file extension match?
"""

import sys
import re
from pathlib import Path

#Add project path
sys.path.insert(0, str(Path(__file__).parent))

from compressor import pipeline


def test_deconstruct_function():
    """Test deconstruct_pdf_to_images function configuration"""
    print("=" * 70)
    print("Test 1: Check deconstruct_pdf_to_images() configuration")
    print("=" * 70)
    
    # Read function source code
    import inspect
    source = inspect.getsource(pipeline.deconstruct_pdf_to_images)
    
    # Check key parameters
    checks = {
        'Use JPEG format': '"-jpeg"' in source or '-jpeg' in source,
        'Set JPEG quality': 'jpegopt' in source or 'quality' in source,
        'Correct file extension': '*.jpg' in source or '.jpg' in source,
    }
    
    all_passed = True
    for check_name, result in checks.items():
        status = "✓ Passed" if result else "✗ Failed"
        print(f"  {check_name:30s}: {status}")
        if not result:
            all_passed = False
    
    #Extract quality parameters
    quality_match = re.search(r'quality[=\s]+(\d+)', source)
    if quality_match:
        quality = quality_match.group(1)
        print(f"\n Detected JPEG quality: {quality}")
        
        if 75 <= int(quality) <= 90:
            print(f" ✓ The quality parameters are reasonable (recommended range: 75-90)")
        else:
            print(f" ⚠️ Quality parameter {quality} may need to be adjusted")
    
    return all_passed


